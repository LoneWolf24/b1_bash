#!/bin/bash
clear
############# PRINTING MY NAME OFF ######################
sh ./practices/myName.sh
#########################################################
echo "          This is a practice for conditionals";
echo "";


##########################################################
#########################################################
######################## FOR LOOP #######################
## Starts at 0 and counts up to 10 ######################
echo "      For Loop"
for count in {0..10};
    do
        echo "The count = $count";
        let count=count+1;
done
echo "";
##########################################################
echo "      For Loop (counting by 2)"
# {starts..end..increments}
for count in {0..10..2};
    do
        echo "The count = $count";
done
echo "";
##########################################################
echo "      For Loop (counting backwards)"
# {starts..end..increments}
for count in {10..0..1};
    do
        echo "The count = $count";
done
echo "";
#########################################################
#########################################################
#########################################################
##########################################################



#########################################################
################# WHILE LOOP ############################
## Starts at 0 and counts up until it is less than 10 ###
echo "      While Loop";
wCount=0
## -lt means less than
while [ $wCount -lt 10 ];
    do
        echo "$wCount";
        let wCount=$wCount+1;
done
echo "";
#########################################################
#########################################################
#########################################################



#########################################################
############### DO WHILE or UNTILL LOOP #################
#Starts at 0 and counts up until uCount is greater than 10
uCount=0
echo "      Until Loop";
## -gt means greater than
until [ $uCount -gt 10 ];
    do
        echo "$uCount";
        let uCount=$uCount+1;
done

echo "";
#########################################################
#########################################################
#########################################################



#########################################################
############# END OF PROGRAM ############################
sh ./practices/goodbye.sh
